export type TranslationKey = keyof typeof messages.en;

export const tFor = (language: Language, key: TranslationKey, values?: Record<string, string | number>) => {
  let text: string = messages[language]?.[key] ?? messages.en[key] ?? key;
  if (!values) return text;
  Object.entries(values).forEach(([name, value]) => {
    text = text.replaceAll(`{${name}}`, String(value));
  });
  return text;
};

export const useT = () => {
  const language = useUiStore((s) => s.language);
  return (key: TranslationKey, values?: Record<string, string | number>) => tFor(language, key, values);
};
